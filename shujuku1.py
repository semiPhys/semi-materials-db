# ---------------------- 智能匹配函数：支持中文/化学式/简写查询 ----------------------
def match_material(query):
    query = query.strip().lower()
    if not query:
        return None
    # 遍历所有材料，匹配中文或化学式
    for full_name, info in SEMICONDUCTOR_DB.items():
        # 提取中文名称和化学式
        cn_name = re.split(r'[()]', full_name)[0]
        en_symbol = re.findall(r'\((.*?)\)', full_name)
        en_symbol = en_symbol[0].lower() if en_symbol else ""
        # 匹配中文、化学式、简写
        if query in cn_name.lower() or query in en_symbol:
            return full_name, info
    return None, None

